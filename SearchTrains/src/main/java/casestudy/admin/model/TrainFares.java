package casestudy.admin.model;

public class TrainFares {
	public int ac1Fare;
	public int ac2Fare;
	public int ac3Fare;
	public int sleeperFare;
	
	public TrainFares() {}

	
	public int getAc2Fare() {
		return ac2Fare;
	}


	public void setAc2Fare(int ac2Fare) {
		this.ac2Fare = ac2Fare;
	}


	public int getAc3Fare() {
		return ac3Fare;
	}


	public void setAc3Fare(int ac3Fare) {
		this.ac3Fare = ac3Fare;
	}


	public int getAc1Fare() {
		return ac1Fare;
	}

	public void setAc1Fare(int ac1Fare) {
		this.ac1Fare = ac1Fare;
	}

	public int getSleeperFare() {
		return sleeperFare;
	}

	public void setSleeperFare(int sleeperFare) {
		this.sleeperFare = sleeperFare;
	}

}
